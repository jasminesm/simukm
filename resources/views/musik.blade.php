<p>Organisasi ini pada awalnya bernama UKM Musik.Organisaisi ini berawal dari perkumpulan mahasiswa-mahasiswi pecinta musik di saat jam kosong perkuliahan, 
berbagi pengetahuan dan bercerita pengalaman tentang musik hingga pada akhirnya, muncul ide untuk mendirikan suatu organisasi musik.</p>
 <h2>VISI</h2>
            <p>KKM Musik sebagai organisasi di bidang seni musik berupaya menciptakan musisi-musisi kreatif, mengembangkan minat & 
            bakat dalam bermusik serta menghasilkan mahasiswa yang berkualitas dalam berorganisasi.</p>

         <h2>MISI</h2>
            <p>1. Membentuk dan menjalin hubungan yang baik antara penikmat maupun pecinta musik untuk saling berbagi.</p>
            <p>2. Menjadi sarana menyalurkan minat dan bakat mahasiswa di bidang seni musik.</p>
            <p>3. Memperkuat nilai-nilai positif serta etika dalam berorganisasi.</p>
            <p>4. Membuat program-program berencana dalam aktifitas bermusik yang bermanfaat, baik di dalam, maupun diluar kampus.</p>
