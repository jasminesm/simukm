<p>Dalam kegiatan non akademik, Basketball kini hadir sebagai  salah satu Unit Kegiatan Mahasiswa yang merupakan wadah untuk menampung dan mengembangkan olahraga basket kampus. UKM ini resmi berdiri pada tanggal 3 Januari 2014. UKM Basket ini didirikan oleh Alan Adi Pratama, melalui gagasan utamanya yaitu ingin memajukan basket kampus, maka didirikannya UKM Basket ini.
Kegiatan Basket ini dilaksanakan setiap hari Jumat pukul 20.00 malam. Bertempat di SMP 26 Kepatihan Surakarta. Dalam mengekspresikan bakat dan minat diharapkan melalui UKM Basket ini kami bisa mengikuti lomba-lomba basket antar kampus yang setiap tahunnya di adakan oleh pihak penyelenggara. SINUS Basketball telah berjalan hingga saat ini. Dengan semangat kita untuk berkompetisi dan membesarkan UKM Basket ini sendiri, UKM ini sudah mulai dikenal di kalangan pebasket kota Surabaya.
