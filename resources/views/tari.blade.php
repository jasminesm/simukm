<p>UKM Tari merupakan suatu wadah seni yang bertujuan untuk melestarikan seni budaya serta menghimpun dan menyalurkan minat dan bakat dari mahasiswa dalam bidang seni sehingga nantinya dapat bermanfaat. Melalui visinya “mewujudkan dan melestarikan seni dan budaya” UKM Kesenian Daerah menjadi satu-satunya UKM yang bergerak dalam pelestarian seni khususnya sni tari yang ada di Politeknik Elektronika Negeri Surabaya. UKM tari berada di bawah naungan BEM PENS. UKM seni tari memiliki misi yaitu meningkatkan meningkatkan kualitas dan kuantitas kader yang memiliki intelektual dan loyalitas dalam bidang seni tari, serta berperan aktif dalam kegiatan kesenian daerah dalam pengabdian pada masyarakat. Anggota UKM Tari merupakan himpunan mahasiswa yang masih aktif di Politeknik Elektronika Negeri Surabaya. UKM Tari memiliki banyak program-program unggulan yang nantinya dapat menjadi pembelajaran skill dan edukatif bagi mahasiswa selain pengetahuan inti yang didapatkan dari perkuliahan. Program tersebut meliputi lomba-lomba, pelatihan dan pembinaan, sosial dan masih banyak yang lainnya. Pelaksanaan program kerja ini yaitu dalam kurun waktu kepengurusan tiap tahunnya.</p>
 <h2>VISI</h2>
            <p>Menjadikan sebagai wadah pembentukan karakter mahasiswa seni tari yang cerdas, aktif dan kreatif.</p>

         <h2>MISI</h2>
            <p>1. Membentuk sikap aktif dan tanggap bagi mahasiswa seni tari dalam segala kegiatan kampus</p>
            <p>2. Menanamkan sikap peduli terhadap hubungan kekerabatan antar mahasiswa program studi, jurusan,fakultas maupun universitas</p>
            <p>3. Mengapresiasi karya seni tari yang dicurahkan melalui demonstrasi karya tari</p>
            <p>4. Memberikan pembelajaran seni tari kepada masyarakat internal maupun eksternal</p>
            <p>5. Menciptakan suasana belajar seni yang kondusif dan antusisas</p>
