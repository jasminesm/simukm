                <h2>Sejarah</h2>
                <p>UKM Futsal adalah jenis UKM yang bergerak di bilang olahraga terutama bola (futsal).
                 UKM ini berdiri sejak 2011 dan hanya mengikuti kegiatan-kegiatan yang ada guna menyalurkan minat dan bakat mahasiswa. 
                 Namun pada tahun 2015 UKM Futsal tidak hanya mengikuti perlombaan atau kompetisi yang ada, tetapi telah mengadakan kegiatan Turnamen Futsal se-Jateng. 
                 Kegiatan pertama di awal tahun 2015 ini adalah Unika Cup yang berorientasi pada Anak-Anak SMA, SMK, ataupun MA yang ada di kabupaten atau kota surabaya. 
                 Dengan berhasilnya kegiatan Unika Cup 2015 ini, para pengurus baru mulai mengembangkan kegiatan di tingkat Fakultas 
                 guna mempererat hubungan dan mengetahui antar Fakultas. Dan beberapa tahun terakhir para pengurus mengadakan 
                kegiatan Bakti Sosial yang merupakan wujud dari rasa syukur UKM Futsal atas terlaksananya kegiatan-kegiatan tanpa kendala ataupun ditentang pihak manapun. 
                Olehkarena itu dari tahun 2015 hingga sekarang UKM Futsal masih mengadakan kegiatan-kegiatan tersebut dan berjalan lancar serta dapat membawa nama baik Unika 
                di mata masyarakat .</p>    
